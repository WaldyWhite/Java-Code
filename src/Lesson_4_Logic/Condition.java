package Lesson_4_Logic;

public class Condition {
    public static void main(String[] args) {
        int x = 5;
        int y = 6;
        if (x > y) {
            System.out.println("X Больше чем  Y");
        } else if (x == y) {
            System.out.println("X равен Y");
        } else {
            System.out.println("Y больше X");
        }
    }
}
